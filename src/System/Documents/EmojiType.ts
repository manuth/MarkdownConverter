/**
 * Represents an emoji-type.
 */
export enum EmojiType
{
    /**
     * Indicates no emoji-type.
     */
    None,

    /**
     * Indicates native emojis.
     */
    Native,

    /**
     * Indicates emojis from GitHub.
     */
    GitHub,

    /**
     * Indicates emojis from Twitter.
     */
    Twitter
}
